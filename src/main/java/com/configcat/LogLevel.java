package com.configcat;

/**
 * Describes the log level.
 */
public enum LogLevel {
    /**
     * Debug level.
     */
    DEBUG,
    /**
     * Info level.
     */
    INFO,
    /**
     * Warning level.
     */
    WARNING,
    /**
     * Error level.
     */
    ERROR,
    /**
     * Turns logging off.
     */
    NO_LOG,
}
